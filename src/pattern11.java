public class pattern11 {
    public static void main(String[] args) {
        int n=8;

        for (int i=0;i<n;i++){
            System.out.print("*");
            for (int j=0;j==3;j++){
                System.out.print("*");
            }
            System.out.print(" ");
            System.out.print(" ");

        }

    }
}
